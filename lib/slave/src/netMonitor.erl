-module(netMonitor).

%% API
-export([get_net_stats/0]).

%%%===================================================================
%%% API
%%%===================================================================

get_net_stats() ->
    A = os:cmd("ifconfig eth0 | grep bytes"),
    B = string:tokens(A, ":"),
    C = hd(string:tokens(hd(tl(B)), " ")),
    D = hd(string:tokens(hd(tl(tl(B))), " ")),
    {C, D}.

